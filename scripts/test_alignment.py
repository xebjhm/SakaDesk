def pp(label, segs):
    print(f"\n=== {label} ({len(segs)} segs) ===")
    for i, s in enumerate(segs):
        m = "" if not s.text or s.text[-1] in "。！？、.!?,\n" else "  ← MID"
        print(f"[{i:2d}] {s.start:6.2f}-{s.end:6.2f} | {s.text}{m}")
    cuts = sum(1 for s in segs if s.text and s.text[-1] not in "。！？、.!?,\n")
    print(f"Mid-cuts: {cuts}/{len(segs)}")


